my_string = input('Введите пароль: ') # Ввод значения
print(len(my_string)) # Подсчет количества символов введенного значения
print(my_string.upper()) # Вывод введенного значения в верхнем регистре
print(my_string.lower()) # Вывод введенного значения в нижнем регистре
print(my_string.replace(' ', '')) # Вывод введенного значения без пробелов
print(my_string[0]) # Вывод первого символа введенного значения
print(my_string[-1]) # Вывод последнего символа введенного значения
